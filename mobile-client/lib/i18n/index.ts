export function translateIncomeSourceName(
  name: string | null | undefined,
  language: string | null | undefined,
): string {
  const rawName = String(name ?? "").trim();
  if (!rawName) return rawName;

  const normalizedName = rawName.toLowerCase();
  const key = (() => {
    if (normalizedName === "salary") return "income.source.salary" as const;
    if (normalizedName === "business income") return "income.source.businessIncome" as const;
    if (normalizedName === "freelance") return "income.source.freelance" as const;
    if (normalizedName === "benefits") return "income.source.benefits" as const;
    if (normalizedName === "investment income") return "income.source.investmentIncome" as const;
    if (normalizedName === "mixed income") return "income.source.mixedIncome" as const;
    return null;
  })();

  return key ? translateAppText(language, key) : rawName;
}